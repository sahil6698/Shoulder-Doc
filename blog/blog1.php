                    <p>I trained and practiced extensively in India (1983-2004) and abroad (2004-2009) doing complex trauma, and elective orthopaedic procedures. Unfortunately, there was hardly any mention of shoulder problems. There were only three conditions which we heard about while doing our specialisation namely: Fractures of ball of shoulder joint, humeral head, managed majorly by not intervening, sometimes with bent very thin plates and wires/sutures due to the lack of modern locking plates. Second diagnosis which we heard about was recurring dislocation of the shoulder in the young population and all of them received only physiotherapy whereas surgeons in France were stabilising these recurrent dislocations by operating for last many decades! Third diagnosis used to be Frozen shoulder which received a steroid injection into the joint (without even knowing where to put that needle as no one knew where the exact pathology was!).</p>
