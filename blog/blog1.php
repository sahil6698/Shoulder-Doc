                    <p>There was no mention of the most important structures inside the shoulder joint which are absolutely essential for a person to lift the arm above the shoulder level. These are a group of four muscles, tendons of which cover the ball and socket joint all around so that we can move the arm in any direction and still not dislocate our shoulder as the socket is only one inch in dimensions and ball is three inch in diameter so much so that only 22% of the surfaces are in contact at any given point of time. These are called Rotator cuff muscles/tendons and no other joint in the body has so many muscles inside the joint except for one tendon of popliteus muscle traversing the knee from inside and helping in stabilising the joint.</p>
                    <br>
