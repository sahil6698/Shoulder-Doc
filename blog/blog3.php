                    <h1>Shoulder Joint - Problems and Management</h1>
                    <span class="dat-sec">post by <strong>Dr Raman Kant Aggarwal</strong> on <strong>May 27th, 2018</strong></span>
                    <p><strong>Shoulder joint</strong> is the most mobile and hence most dislocating, ball and socket joint. There are tendons (rotator cuff & biceps)inside the joint to compress the ball into the socket for stability. In addition, ligaments around the socket provide stability at the extreme range of movements like throwing which is the fastest human motion on earth. This makes shoulder prone to various injuries as well as faster wear and tear of tendons, cartilage and bone.</p>
                    <!--======= Details Sec =========-->
                    <img src="../images/blog/blog3.jpg" class="img-responsive">
                    <br>
                    <p>Shoulder being a complex of 5 joints and overlay of cardiac and cervical pain makes the diagnosis a challenge. Cardiac pain starts from Left side of chest, radiates to innerside of arm, (distribution in C7,8-T1). Cervical spine pain radiates below elbow to fingers, patient is better in lying down position, trapezial irritation is present, Spurling sign is positive & neurological signs may be present. Shoulder pain gets referred to outer side of upper arm, distribution being the same as in cervical pain but in contrast it increases on lying down (increasing even more in the night) and is better in sitting posture.</p>
                    <br>
                    <h3>Shoulder Problems</h3>
                    <p>Problems of stability-Traumatic dislocation / Atraumatic instability / Labral (Ligament) tears like Bankart lesion/SLAP: These are the most common sports injuries of the shoulder and produce instability. They start in teen age and never heal by themselves until surgically repaired. Nowadays, all such ligamentous injuries of the shoulder are treated with arthroscopic technique (Key-hole surgery)and this gives an edge over open technique. In many cases, labrum (Ligament) is detached from the socket all around 360⁰ which cannot be seen by open surgery and even highest quality of MRI and expertise in imaging falls short of diagnosing them pre-operatively. If we don’t treat such injuries, they become recurrent in more than 99% of cases and shoulder keeps on dislocating, compromising the quality of life severely.</p>
                    <br>
                    <p>Problems of Motion-Frozen shoulder / Post-traumatic or stiffness: This is not the commonest condition. This is a self-recovering condition and happens more frequently in patients with diabetes, thyroid disorders and after heart/breast surgeries. Essential criteria for diagnosis are-1) Restricted active and passive movements in all directions 2) No history of surgery/injury 3) True AP and Axillary x-rays of the shoulder must be normal to rule out arthritis which behaves in a similar fashion. One good thing about frozen shoulder is that it rarely happens again in the same shoulder.</p>
                    <br>
                    <p style="font-style: italic;"><strong>So if your shoulder is stiff for more than 3 years, chances are that you are having arthritis of the shoulder and not frozen shoulder. Also, if you are getting frozen shoulder time and again, then you are having rotator cuff disease/tear and not frozen shoulder.</strong></p>
                    <br>
                    <p>Problems of smoothness-Impingement is the friction between rotator cuff tendons and undersurface of shoulder blade. There is no stiffness but pain at certain angles. This can be successfully treated with medicines, steroid injection and exercises. Untreated, it may progress to tear of the tendons.Arthritis of the shoulder is of 6 types and is very common but mistakenly passed as frozen shoulder usually. Every 14th patient of bilateral knee osteo-arthritis is having shoulder arthritis. Moreover, end stage of rotator cuff tears also leads to arthritis which can be successfully treated with anatomical / reverse shoulder replacement. Our unit, at FBJI, is leading in shoulder replacement in Northern India.</p>
                    <br>
                    <p>Problems of Strength- Incomplete / complete tears of the rotator cuff tendons is indeed the commonest problem in the shoulder joint. This goes unrecognized and can lead to constant pain, weakness and loss of motion. Recent advances in arthroscopic techniques and instrumentation has made it possible to repair even largest tears with success.</p>
                    <br>
                    <p style="font-style: italic;"><strong>In conclusion, all the problems of the shoulder can be treated with 95-97% success rate not only in terms of pain relief but function as well but we have to get the diagnosis right, manage it with proper expertise and persevere in rehabilitation.</strong></p>
